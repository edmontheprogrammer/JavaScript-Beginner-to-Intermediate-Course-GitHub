let x = 1;

// Relational Operators: 
// checking if the value of x is greater than 0
console.log(x > 0);
// checking if x is greater than or equal to 1
console.log(x >= 1);
// check if x is less than 1
console.log(x < 1);
// checking if x is less than or equal to 1
console.log(x <= 1);

// Equality Operator '===' signs for equality operator 
console.log(x === 1);
// Checking if x is not equal to a given value
console.log(x !== 1); 
