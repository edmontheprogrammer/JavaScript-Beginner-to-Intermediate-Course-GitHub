// Arithmetic operators 
let x = 10;
let y = 3;

// addition operator 
console.log(x + y);
// subtraction operator 
console.log(x - y);
// multiplicaiton operator 
console.log(x * y);
// division operator 
console.log(x / y);
// remainder of division 
console.log(x % y);
// exponentiation operator; this is x to the power of y
console.log(x ** y);

// Increment Operator (++)
// ++x; preincrement operator 
// x++; postincrement operator 
console.log(x++);
console.log(x)

// Decrement Operator (--)
// --x; pre-decrement operator 
// x--; post-decrement operator 
console.log(--x);


