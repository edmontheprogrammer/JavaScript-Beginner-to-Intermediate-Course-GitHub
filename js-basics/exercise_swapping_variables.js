// Swapping Variables 
// Write code that will swap the values of 'a' and 'b' so that the 
// output will be 'blue' and 'red'
let a = 'red';
let b = 'blue';
let c = a; // c now contains the value of 'a'
a = b; // now a contains the value of b
b = c; // now c contains the value of a

console.log(a); // should return 'blue'
console.log(b); // should return 'red'

